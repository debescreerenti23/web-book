/**
 * Función para cerrar la portada e iniciar la experiencia
 */
function abrirLibro() {
    // Reproducir sonido al abrir
    sonidoPagina.play();
    
    // Añadimos la clase para que la portada suba (definida en tu CSS)
    portada.classList.add('ocultar-portada');
    
    // Si no hay nada cargado, cargamos el capítulo 1 por defecto
    if (display.innerHTML.includes("Selecciona una aventura")) {
        cargarCapitulo(1);
    }
}

/**
 * Función para renderizar un capítulo específico
 */
function cargarCapitulo(id) {
    const cap = capitulos.find(c => c.id === id);
    if (!cap) return;

    // Sonido cada vez que cambiamos de historia
    sonidoPagina.play();

    // 1. Aplicamos una clase de "salida" o simplemente bajamos la opacidad
    display.style.opacity = 0;

    // 2. Esperamos un poco (mientras el scroll sube) y cambiamos el contenido
    setTimeout(() => {
        display.innerHTML = `
            <article class="entrada-libro">
                <img src="${cap.imagen}" alt="${cap.titulo}" id="img-relato" onerror="this.style.display='none'">
                <h2>${cap.titulo}</h2>
                <p class="tag-mascota">🐾 Protagonista: <strong>${cap.personajePrincipal}</strong></p>
                <div class="texto-relato">
                    ${cap.texto.replace(/\n/g, '<br>')} 
                </div>
                
                <div class="navegacion-inferior">
                    ${id < capitulos.length ? 
                        `<button class="btn-siguiente" onclick="cargarCapitulo(${id + 1})">Siguiente Aventura ➔</button>` : 
                        `<p class="fin-cuento">✨ ¡Has llegado al final de las aventuras por hoy! ✨</p>`
                    }
                </div>
            </article>
        `;
        
        // 3. Devolvemos la opacidad para el efecto visual
        display.style.opacity = 1;
        window.scrollTo({ top: 0, behavior: 'smooth' });
    }, 300);
}

/**
 * Inicialización del menú de navegación superior
 */
function generarMenu() {
    capitulos.forEach(cap => {
        const boton = document.createElement('button');
        boton.innerText = `Cap. ${cap.id}`;
        boton.title = cap.titulo;
        boton.onclick = () => cargarCapitulo(cap.id);
        menuNav.appendChild(boton);
    });
}

